<script>

    // JavaScript for increment and decrement functionality
    document.addEventListener("DOMContentLoaded", function () {
        const quantitySelectors = document.querySelectorAll(".quantity-selector");

        quantitySelectors.forEach(function (selector) {
            const decreaseButton = selector.querySelector(".decrease");
            const increaseButton = selector.querySelector(".increase");
            const quantityInput = selector.querySelector("input");

            decreaseButton.addEventListener("click", function () {
                let value = parseInt(quantityInput.value);
                if (value > 1) {
                    value--;
                    quantityInput.value = value;
                }
            });

            increaseButton.addEventListener("click", function () {
                let value = parseInt(quantityInput.value);
                value++;
                quantityInput.value = value;
            });
        });
    });




</script>



    


